## types of function
# parameter O, return value O
def sum(a, b):
    return a + b
result_sum = sum(1, 2)
# print(result_sum)

# parameter O, return value X
def hi(name):
    print("Hello, " + name)
# hi("marco")

# parameter X, return value O
def three():
    return 3
result_three = three()
# print(result_three)

# parameter X, return value X
def hello():
    print("Hello!")
# hello()


